package com.sdn.model;

public class NbRouterInfo {
	
		
	    
	    private String nb_Ip;
	    private String delay;
	    private String pktloss;
	    private String ss;
	    
	   
	   
		public String getNb_Ip() {
			return nb_Ip;
		} 
		public void setNb_Ip(String nb_Ip) {
			this.nb_Ip = nb_Ip;
		}
		
		public String getDelay() {
			return delay;
		}
		public void setDelay(String delay) {
			this.delay = delay;
		}
		
		public String getPktloss() {
			return pktloss;
		}
		public void setPktloss(String pktloss) {
			this.pktloss = pktloss;
		}
		
		public String getSS() {
			return ss;
		}
		public void setSS(String ss) {
			this.ss = ss;
		}
		
	
		

	

}
